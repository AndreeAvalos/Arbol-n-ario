package arbol.n.ario;

/**
 *
 * @author Andree
 */
public class dato {
    private int numero;
    private String dato;

    public dato(int numero, String dato) {
        this.numero = numero;
        this.dato = dato;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getDato() {
        return dato;
    }

    public void setDato(String dato) {
        this.dato = dato;
    }
    
    
}
